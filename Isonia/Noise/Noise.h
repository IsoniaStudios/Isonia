	class Noise
	{
	public:
		Noise(int seed = 69)
		{
			
		}

        float GeneratePerlinNoise(const int seed, const float x, const float y) const
        {
            int x0 = IMath::FloorToInt(x);
            int y0 = IMath::FloorToInt(y);

            float xd0 = (float)(x - x0);
            float yd0 = (float)(y - y0);
            float xd1 = xd0 - 1;
            float yd1 = yd0 - 1;

            float xs = IMath::InterpolationQuintic(xd0);
            float ys = IMath::InterpolationQuintic(yd0);

            x0 *= PrimeX;
            y0 *= PrimeY;
            int x1 = x0 + PrimeX;
            int y1 = y0 + PrimeY;

            float xf0 = IMath::Lerp(GradCoord(seed, x0, y0, xd0, yd0), GradCoord(seed, x1, y0, xd1, yd0), xs);
            float xf1 = IMath::Lerp(GradCoord(seed, x0, y1, xd0, yd1), GradCoord(seed, x1, y1, xd1, yd1), xs);

            return IMath::Lerp(xf0, xf1, ys) * 1.4247691104677813f;
        }

	private:
		int seed;
        float frequency;

        static const int PrimeX = 501125321;
        static const int PrimeY = 1136930381;
        static const int PrimeZ = 1720413743;

        static int Hash(const int seed, const int xPrimed, const int yPrimed)
        {
            int hash = seed ^ xPrimed ^ yPrimed;

            hash *= 0x27d4eb2d;
            return hash;
        }

        static int Hash(const int seed, const int xPrimed, const int yPrimed, const int zPrimed)
        {
            int hash = seed ^ xPrimed ^ yPrimed ^ zPrimed;

            hash *= 0x27d4eb2d;
            return hash;
        }

        static float ValCoord(const int seed, const int xPrimed, const int yPrimed)
        {
            int hash = Hash(seed, xPrimed, yPrimed);

            hash *= hash;
            hash ^= hash << 19;
            return hash * (1 / 2147483648.0f);
        }

        static float ValCoord(const int seed, const int xPrimed, const int yPrimed, const int zPrimed)
        {
            int hash = Hash(seed, xPrimed, yPrimed, zPrimed);

            hash *= hash;
            hash ^= hash << 19;
            return hash * (1 / 2147483648.0f);
        }

        static float GradCoord(const int seed, const int xPrimed, const int yPrimed, const float xd, const float yd)
        {
            int hash = Hash(seed, xPrimed, yPrimed);
            hash ^= hash >> 15;
            hash &= 127 << 1;

            float xg = Gradients2D[hash];
            float yg = Gradients2D[hash | 1];

            return xd * xg + yd * yg;
        }
	};
